# Operadores relacionales básicos

a = 5
b = 3

print("a == b:", a == b) # a es igual a b? False
print("a != b:", a != b) # a es diferente de b? True
print("a > b:", a > b)   # a es mayor que b? True
print("a < b:", a < b)   # a es menor que b? False
print("a >= b:", a >= b) # a es mayor o igual que b? True
print("a <= b:", a <= b) # a es menor o igual que b? False